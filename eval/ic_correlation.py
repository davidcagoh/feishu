#!/usr/bin/env python3
"""
IC correlation matrix across daily signals.

Computes the Spearman IC time series for each daily signal, then shows:
  - pairwise correlation of IC series
  - mean IC and IR per signal (confirmation)

Usage:
    python eval/ic_correlation.py          # full data
    python eval/ic_correlation.py --sample # sample data (noisy, smoke-test only)

Output saved to wiki/results/ic_correlation.md
"""

import argparse
import sys
from datetime import date
from pathlib import Path

import numpy as np
import pandas as pd
from scipy.stats import spearmanr

sys.path.insert(0, str(Path(__file__).parent.parent))
from signals import REGISTRY

DAILY_SIGNALS = ["short_term_reversal", "price_to_vwap", "volume_reversal",
                 "alpha191_046", "alpha191_071"]


def load_daily(sample: bool) -> pd.DataFrame:
    root = Path(__file__).parent.parent / "data"
    path = root / ("daily_sample.parquet" if sample else "daily_data_in_sample.parquet")
    return pd.read_parquet(path)


def compute_returns(daily: pd.DataFrame) -> pd.DataFrame:
    df = daily.copy().sort_values(["asset_id", "trade_day_id"])
    df["adj_close"] = df["close"] * df["adj_factor"]
    df["ret"] = df.groupby("asset_id")["adj_close"].pct_change()
    df.loc[df["ret"].abs() > 0.095, "ret"] = np.nan
    return df.pivot(index="trade_day_id", columns="asset_id", values="ret")


def ic_series(signal: pd.DataFrame, returns: pd.DataFrame) -> pd.Series:
    """Per-day Spearman IC, signal[t] predicts returns[t+1]."""
    sig_shifted = signal.shift(1)
    common_days = sig_shifted.index.intersection(returns.index)
    common_assets = sig_shifted.columns.intersection(returns.columns)
    sig = sig_shifted.loc[common_days, common_assets]
    ret = returns.loc[common_days, common_assets]

    ics = {}
    for day in common_days:
        s = sig.loc[day].dropna()
        r = ret.loc[day].dropna()
        common = s.index.intersection(r.index)
        if len(common) < 20:
            continue
        ic, _ = spearmanr(s[common].values, r[common].values)
        if not np.isnan(ic):
            ics[day] = ic
    return pd.Series(ics, name=signal.columns.name or "signal")


def build_report(ic_df: pd.DataFrame, corr: pd.DataFrame, sample: bool) -> str:
    today = date.today().isoformat()
    n_days = len(ic_df)

    lines = [
        f"# IC Correlation Matrix — {today}",
        "",
        f"**Signals:** {', '.join(ic_df.columns)}",
        f"**Days with IC:** {n_days}  {'(sample — noisy)' if sample else '(full dataset)'}",
        "",
        "## Per-Signal Summary",
        "",
        "| Signal | Mean IC | IC Std | IR (ann.) | Hit Rate | Days |",
        "|--------|---------|--------|-----------|----------|------|",
    ]

    for col in ic_df.columns:
        s = ic_df[col].dropna()
        n = len(s)
        mean_ic = s.mean() if n > 0 else np.nan
        std_ic = s.std() if n > 1 else np.nan
        ir = mean_ic / std_ic * np.sqrt(252) if (n > 1 and std_ic > 0) else np.nan
        hit = (s > 0).mean() if n > 0 else np.nan
        lines.append(
            f"| {col} | {mean_ic:.4f} | {std_ic:.4f} | {ir:.2f} | {hit:.0%} | {n} |"
        )

    lines += [
        "",
        "## IC Correlation Matrix",
        "",
        "Pairwise Pearson correlation of daily IC time series (higher overlap days).",
        "",
    ]

    # Header row
    cols = list(corr.columns)
    header = "| Signal | " + " | ".join(cols) + " |"
    sep = "|--------|" + "|".join(["-------"] * len(cols)) + "|"
    lines += [header, sep]
    for row_name in corr.index:
        vals = " | ".join(f"{corr.loc[row_name, c]:.3f}" for c in cols)
        lines.append(f"| {row_name} | {vals} |")

    lines += [
        "",
        "## Interpretation",
        "",
        "- **|r| < 0.3**: signals are largely independent → combining adds IR",
        "- **|r| 0.3–0.6**: moderate overlap → combination still beneficial",
        "- **|r| > 0.6**: high overlap → diminishing returns from combination",
        "",
    ]

    # Auto-commentary
    off_diag = []
    for i, c1 in enumerate(cols):
        for c2 in cols[i+1:]:
            off_diag.append((c1, c2, corr.loc[c1, c2]))

    low_corr = [(a, b, r) for a, b, r in off_diag if abs(r) < 0.3]
    high_corr = [(a, b, r) for a, b, r in off_diag if abs(r) > 0.6]

    if low_corr:
        pairs = ", ".join(f"{a}↔{b} ({r:.3f})" for a, b, r in low_corr)
        lines.append(f"**Low-correlation pairs (combine freely):** {pairs}")
    if high_corr:
        pairs = ", ".join(f"{a}↔{b} ({r:.3f})" for a, b, r in high_corr)
        lines.append(f"**High-correlation pairs (redundant):** {pairs}")

    lines += ["", "---", f"_Generated by `eval/ic_correlation.py` on {today}_"]
    return "\n".join(lines)


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--sample", action="store_true")
    args = parser.parse_args()

    print("Loading data...", flush=True)
    daily = load_daily(args.sample)
    returns = compute_returns(daily)

    ic_dict: dict[str, pd.Series] = {}
    for name in DAILY_SIGNALS:
        if name not in REGISTRY:
            print(f"  [skip] {name} not in REGISTRY")
            continue
        print(f"  Computing IC series for {name}...", flush=True)
        signal = REGISTRY[name].compute(daily, None)
        s = ic_series(signal, returns)
        s.name = name
        ic_dict[name] = s

    ic_df = pd.DataFrame(ic_dict)
    # Drop days where any signal is missing (for a clean correlation)
    ic_df_aligned = ic_df.dropna()
    print(f"\n  Aligned days (all signals present): {len(ic_df_aligned)}")

    corr = ic_df_aligned.corr(method="pearson")

    print("\n=== IC Correlation Matrix ===")
    print(corr.round(3).to_string())
    print("\n=== Per-Signal Mean IC ===")
    print(ic_df.mean().round(4).to_string())

    report = build_report(ic_df, corr, args.sample)

    results_dir = Path(__file__).parent.parent / "wiki" / "results"
    results_dir.mkdir(parents=True, exist_ok=True)
    out_path = results_dir / "ic_correlation.md"
    out_path.write_text(report)
    print(f"\nSaved to {out_path}")


if __name__ == "__main__":
    main()
